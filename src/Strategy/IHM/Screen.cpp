const unsigned char _LOGO_KARIBOUS_width = 128;
const unsigned char _LOGO_KARIBOUS_height = 33;

void Screen::init()
{
  lcd.begin();
  prepare();
  splashScreen();
}

void Screen::prepare()
{
  lcd.setFont(u8g2_font_4x6_tf);
  lcd.setFontRefHeightExtendedText();
  lcd.setDrawColor(1);
  lcd.setFontPosTop();
  lcd.setFontDirection(0);
}

void Screen::splashScreen()
{
  lcd.clearBuffer();
  lcd.drawXBMP(0, 15, _LOGO_KARIBOUS_width, _LOGO_KARIBOUS_height, LOGO_KARIBOUS_bits);
  lcd.sendBuffer();
}

void Screen::initMsg()
{
  Screen::lcd.clearBuffer();
  Screen::lcd.setFont(u8g2_font_logisoso58_tr);
  Screen::lcd.drawStr(10, 2, "Init");
  Screen::lcd.sendBuffer();
}

